function createProductCard(product) {
    let card = document.createElement("div");
    card.classList.add("product-card");
  
    let image = document.createElement("img");
    image.classList.add("product-img");
    image.src = product.image;
    image.alt = product.name;
    card.appendChild(image);
  
    let sizeAvailability = document.createElement("p");
    sizeAvailability.classList.add("product-size");
    sizeAvailability.textContent = product.sizes;
    card.appendChild(sizeAvailability);
  
    let productName = document.createElement("h3");
    productName.classList.add("product-name");
    productName.textContent = product.name;
    card.appendChild(productName);
  
    let Manufactured = document.createElement("p");
    Manufactured.classList.add("product-manufacturer");
    Manufactured.textContent = product.manufacturer;
    card.appendChild(Manufactured);
  
    let price = document.createElement("p");
    price.classList.add("product-price");
    price.textContent = `$${product.price}`;
    card.appendChild(price);
  
    let rating = document.createElement("p");
    rating.classList.add("product-rating");
    rating.textContent = `Rating: ${product.rating}`;
    card.appendChild(rating);
  
    let deliveryDays = document.createElement("p");
    deliveryDays.classList.add("product-delivery");
    deliveryDays.textContent = product.deliveryDays;
    card.appendChild(deliveryDays);
  
    let addToCartButton = document.createElement("button");
    addToCartButton.textContent = "Add to Cart";
    addToCartButton.classList.add("add-to-cart-btn");
    addToCartButton.addEventListener("click", function() {
        console.log("Product added to cart:", product.name);
    });
    card.appendChild(addToCartButton);

    return card;
  }
  
  function addProductCards(productsToShow) {
    let container = document.getElementById("container");
    container.innerHTML = ""; 
  
    productsToShow.forEach((product) => {
      let productCard = createProductCard(product);
      container.appendChild(productCard);
    });
  }
  window.onload = function () {
    let saleMode = false; 
    let priceMode = ""; 
  
    let toggleSaleButton = document.createElement("button");
    toggleSaleButton.classList.add("sale-btn");
    toggleSaleButton.textContent = "Sale";
    toggleSaleButton.addEventListener("click", function () {
      saleMode = !saleMode; 
      if (saleMode) {
        let saleProducts = products.filter((product) => product.sale === true);
        addProductCards(saleProducts);
        toggleSaleButton.textContent = "Sale: ON";
        toggleSaleButton.style.backgroundColor = "rgb(254, 148, 254)";
        toggleSaleButton.style.color = "white";
      } else {
        addProductCards(products);
        toggleSaleButton.textContent = "Sale";
        toggleSaleButton.style.backgroundColor = "";
        toggleSaleButton.style.color = "";
      }
    });
    document.body.insertBefore(
      toggleSaleButton,
      document.getElementById("container")
    );
  
    let togglePriceButton = document.createElement("button");
    togglePriceButton.classList.add("price-btn");
    togglePriceButton.textContent = "Price";
    togglePriceButton.addEventListener("click", function () {
      if (priceMode === "") {
        priceMode = "lowToHigh";
        let sortedProducts = products
          .slice()
          .sort((a, b) => parseFloat(a.price) - parseFloat(b.price));
        if (saleMode) {
          sortedProducts = sortedProducts.filter(
            (product) => product.sale === true
          );
        }
        addProductCards(sortedProducts);
        togglePriceButton.textContent = "Price: LOW to HIGH";
        togglePriceButton.style.backgroundColor = "rgb(254, 148, 254)";
        togglePriceButton.style.color = "white";
      } else if (priceMode === "lowToHigh") {
        priceMode = "highToLow";
        let sortedProducts = products
          .slice()
          .sort((a, b) => parseFloat(b.price) - parseFloat(a.price));
        if (saleMode) {
          sortedProducts = sortedProducts.filter(
            (product) => product.sale === true
          );
        }
        addProductCards(sortedProducts);
        togglePriceButton.textContent = "Price: HIGH to LOW";
        togglePriceButton.style.backgroundColor = "rgb(254, 148, 254)";
        togglePriceButton.style.color = "white";
      } else {
        priceMode = "";
        let productsToShow = saleMode
          ? products.filter((product) => product.sale === true)
          : products;
        addProductCards(productsToShow);
        togglePriceButton.textContent = "Price";
        togglePriceButton.style.backgroundColor = "";
        togglePriceButton.style.color = "";
      }
    });
    document.body.insertBefore(
      togglePriceButton,
      document.getElementById("container")
    );
  
    addProductCards(products);
  };
  