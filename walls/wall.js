window.onload = function () {
  let saleMode = false; 
  let priceMode = ""; 

  let toggleSaleButton = document.createElement("button");
  toggleSaleButton.classList.add("sale-btn");
  toggleSaleButton.textContent = "Sale";
  toggleSaleButton.addEventListener("click", function () {
    saleMode = !saleMode; 
    if (saleMode) {
      let saleProducts = products.filter((product) => product.sale === true);
      addProductCards(saleProducts);
      toggleSaleButton.textContent = "Sale: ON";
      toggleSaleButton.style.backgroundColor = "rgb(254, 148, 254)";
      toggleSaleButton.style.color = "white";
    } else {
      addProductCards(products);
      toggleSaleButton.textContent = "Sale";
      toggleSaleButton.style.backgroundColor = "";
      toggleSaleButton.style.color = "";
    }
  });
  document.body.insertBefore(
    toggleSaleButton,
    document.getElementById("container")
  );

  let togglePriceButton = document.createElement("button");
  togglePriceButton.classList.add("price-btn");
  togglePriceButton.textContent = "Price";
  togglePriceButton.addEventListener("click", function () {
    if (priceMode === "") {
      priceMode = "lowToHigh";
      let sortedProducts = products
        .slice()
        .sort((a, b) => parseFloat(a.price) - parseFloat(b.price));
      if (saleMode) {
        sortedProducts = sortedProducts.filter(
          (product) => product.sale === true
        );
      }
      addProductCards(sortedProducts);
      togglePriceButton.textContent = "Price: LOW to HIGH";
      togglePriceButton.style.backgroundColor = "rgb(254, 148, 254)";
      togglePriceButton.style.color = "white";
    } else if (priceMode === "lowToHigh") {
      priceMode = "highToLow";
      let sortedProducts = products
        .slice()
        .sort((a, b) => parseFloat(b.price) - parseFloat(a.price));
      if (saleMode) {
        sortedProducts = sortedProducts.filter(
          (product) => product.sale === true
        );
      }
      addProductCards(sortedProducts);
      togglePriceButton.textContent = "Price: HIGH to LOW";
      togglePriceButton.style.backgroundColor = "rgb(254, 148, 254)";
      togglePriceButton.style.color = "white";
    } else {
      priceMode = "";
      let productsToShow = saleMode
        ? products.filter((product) => product.sale === true)
        : products;
      addProductCards(productsToShow);
      togglePriceButton.textContent = "Price";
      togglePriceButton.style.backgroundColor = "";
      togglePriceButton.style.color = "";
    }
  });
  document.body.insertBefore(
    togglePriceButton,
    document.getElementById("container")
  );

  addProductCards(products);
};
